package kr.co.mj.vo;

public class Grade {

	private String gra;
	private int lowestNujuk;
	private int highestNujuk;
	
	public Grade() {
		super();
	}

	public String getGra() {
		return gra;
	}

	public void setGra(String gra) {
		this.gra = gra;
	}

	public int getLowestNujuk() {
		return lowestNujuk;
	}

	public void setLowestNujuk(int lowestNujuk) {
		this.lowestNujuk = lowestNujuk;
	}

	public int getHighestNujuk() {
		return highestNujuk;
	}

	public void setHighestNujuk(int highestNujuk) {
		this.highestNujuk = highestNujuk;
	}
	
	
}
