package kr.co.mj.vo;

public class ShortCartInfo {

	private String proName;
	private int cartProAmount;
	
	public ShortCartInfo() {}

	public String getProName() {
		return proName;
	}

	public void setProName(String proName) {
		this.proName = proName;
	}

	public int getCartProAmount() {
		return cartProAmount;
	}

	public void setCartProAmount(int cartProAmount) {
		this.cartProAmount = cartProAmount;
	}
	
}
